# Data Types
# Int - no decimal
123
-425
# Float - has a decimal
123.0
-3.4
# String - use either "" or ''
"hello"
'This is how you include "quotes"'
# Bool - capitalize the word
True    # equal to 1
False   # equal to 0
print(False == 0)

# casting
float_to_int = int(124.5)  # truncates
print(float_to_int)

# variables (dynamically allocated, strongly typed (cant change type later))
name = 'nathan'  # no type specification
print("hello", name)

# var names cant start with a number and the only sp char allowed is '_'
my_age = 30